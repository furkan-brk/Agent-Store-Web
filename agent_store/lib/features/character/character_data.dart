// 0=transparent, 1=primary, 2=secondary, 3=skin, 4=accent, 5=dark,
// 6=highlight, 7=gold/detail, 8=eyes (solid dark), 9=rarity accent
// Painter renders each pixel at size.width/32

// Scale a 16×16 matrix to 32×32 by nearest-neighbour 2× upscale.
List<List<int>> _scale2x(List<List<int>> src) {
  final result = <List<int>>[];
  for (final row in src) {
    final newRow = <int>[];
    for (final px in row) { newRow.add(px); newRow.add(px); }
    result.add(newRow);
    result.add(List<int>.from(newRow));
  }
  return result;
}

